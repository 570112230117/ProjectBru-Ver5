package com.bru.model;

public class TypeBrand {

	private int id;
	private String name;
	private String product;
	private String appliances;
	private String appliancesId;
	private String productId;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getProduct() {
		return product;
	}
	public void setProduct(String product) {
		this.product = product;
	}
	public String getAppliances() {
		return appliances;
	}
	public void setAppliances(String appliances) {
		this.appliances = appliances;
	}
	public String getAppliancesId() {
		return appliancesId;
	}
	public void setAppliancesId(String appliancesId) {
		this.appliancesId = appliancesId;
	}
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	
}
